    INT 21H
    MOV CHAR,AL
    
    CMP AL,'A'
    JB ELSE
    CMP AL,'Z'
    JA ELSE
    MOV AH,2
    MOV DL,CHAR
    INT 21H
    
    ELSE:
    
    
