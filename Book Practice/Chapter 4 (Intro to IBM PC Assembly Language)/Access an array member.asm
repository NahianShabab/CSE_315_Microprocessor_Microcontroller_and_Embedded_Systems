.MODEL SMALL


.STACK 100H


.DATA
CR EQU 0DH
LF EQU 0AH

MESSAGE DB "HELLO!$"

.CODE

MAIN PROC
	;DATA SEGMENT INITIALIZATION
    MOV AX, @DATA
    MOV DS, AX
    
    ;KIND OF LIKE MESSAGE[4],WILL PRINT 'O'
    MOV DL,MESSAGE+4
    
    MOV AH,2
    INT 21H
      
    
    ;DOS EXIT
    MOV AH, 4CH
    INT 21H

MAIN ENDP
END MAIN
