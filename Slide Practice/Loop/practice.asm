.MODEL SMALL


.STACK 100H


.DATA
CR EQU 0DH
LF EQU 0AH

NEWLINE DB CR,LF,'$'

ARR DB 'A','B','C','D','E'



.CODE

MAIN PROC
	;DATA SEGMENT INITIALIZATION
    MOV AX, @DATA
    MOV DS, AX
    
    LEA SI,ARR
    
    MOV BYTE PTR [SI+1],'K'
    
    MOV AH,2
    MOV DL,ARR+1
    INT 21H
    
    
    ;DOS EXIT
    MOV AH, 4CH
    INT 21H

MAIN ENDP
END MAIN
